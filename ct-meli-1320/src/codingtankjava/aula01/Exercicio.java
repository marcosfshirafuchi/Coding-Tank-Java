package codingtankjava.aula01;

public class Exercicio {
    /**
     * <h1> Coding Tank (Java)</h1><br><br>
     * Exercício da 1ª aula:<br><br>
     * Crie duas variáveis, atribua um número inteiro para cada uma, eimprima a saída para cada uma das operadores aritméticas entre as variáveis.<br><br>
     * <p>
     * <b>Note:</b> Desenvolvido na linguagem Java.
     *
     * @author Marcos Ferreira Shirafuchi
     * @version 1.0
     * @since 04/12/2024
     */
    public static void main(String[] args) {
        int a = 10;
        int b = 2;

        System.out.println( a + " +  " + b + " = " + (a + b));
        System.out.println( a + " -  " + b + " = " + (a - b));
        System.out.println( a + " *  " + b + " = " + (a * b));
        System.out.println( a + " /  " + b + " = " + (a / b));
    }
}
